package com.zwy.system;

public class SemesterScore {
    private int mathscore;
    private int englishscore;
    private int introductionscore;
    private int pescore;

    public SemesterScore() {
    }

    public int getMathscore() {
        return mathscore;
    }

    public void setMathscore(int mathscore) {
        this.mathscore = mathscore;
    }

    public int getEnglishscore() {
        return englishscore;
    }

    public void setEnglishscore(int englishscore) {
        this.englishscore = englishscore;
    }

    public int getIntroductionscore() {
        return introductionscore;
    }

    public void setIntroductionscore(int introductionscore) {
        this.introductionscore = introductionscore;
    }

    public int getPescore() {
        return pescore;
    }

    public void setPescore(int pescore) {
        this.pescore = pescore;
    }
}
